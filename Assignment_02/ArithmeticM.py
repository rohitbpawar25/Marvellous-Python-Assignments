# Create a module named Arithmetic, which contains four functions: Add() Sub()  Mult() Div() Each function should accept two parameters and perform the respective arithmetic operation.

def Addition(Num1,Num2):
    return Num1+Num2

def Subtraction(Num1,Num2):
    return Num1-Num2

def Multiplication(Num1,Num2):
    return Num1*Num2

def Division(Num1,Num2):
    return Num1/Num2
